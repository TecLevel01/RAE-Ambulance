package com.oli.raeambulance.object;

public class User {
    String email, uname, phone, hospital, ambNo;
    int accountType;

    public User() {

    }

    public String getEmail() {
        return email;
    }

    public String getUname() {
        return uname;
    }

    public String getPhone() {
        return phone;
    }

    public String getHospital() {
        return hospital;
    }

    public String getAmbNo() {
        return ambNo;
    }

    public int getAccountType() {
        return accountType;
    }
}
